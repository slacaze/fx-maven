function rmsandbox()
    if ~isempty( ver( 'maven' ) )
        fx.maven.command.rmsandbox();
    else
        thisPath = fileparts( mfilename( 'fullpath' ) );
        rmpath( fullfile(...
            thisPath,...
            'code',...
            'myapp' ) );
        rmpath( fullfile(...
            thisPath,...
            'test' ) );
    end
end
